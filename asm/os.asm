TRAP_PUTS_HANDLER
    ST R2, PUTS_R2_SAVE ; holds char ptr 
    AND R2, R2, #0      ; clear R2
    ADD R2, R0, #0      ; transfer ptr to R2
PUTS_LOOP
    LDR R0, R2, #0      ; R0 <- *ptr
    BRz DONE_PUTS       ; if (*ptr == 0) goto DONE_PUTS
    OUT                 ; putc(*ptr)
    ADD R2, R2, #1      ; ptr++
    BRnzp PUTS_LOOP
DONE_PUTS
    LD R2, PUTS_R2_SAVE
    RTI
    
TRAP_OUT_HANDLER
    ST R1, OUT_R1_SAVE
DISPLAYWAIT
    LDI R1, DSR
    BRzp DISPLAYWAIT
    STI R0, DDR         ; char comes in in R0
    LD R1, OUT_R1_SAVE
    RTI



