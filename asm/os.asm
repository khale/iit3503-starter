TRAP_IN_HANDLER
	LEA R0, TRAP_IN_MSG	; ask for input from keyboard
	PUTS
	GETC                ; read in a single char
	OUT                 ; echo back to user
	ST R0, IN_R0_SAVE   ; save the char
	AND R0, R0, #0      ; output newline
	ADD R0, R0, #10
	OUT
	LD R0, IN_R0_SAVE   ; restore the char
	RTI

